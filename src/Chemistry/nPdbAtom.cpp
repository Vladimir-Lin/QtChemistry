int N::ProteinBank::Atom::code(void) const
{
  return Code ;
}

void N::ProteinBank::Atom::setCode(int c)
{
  Code = c ;
}

void N::ProteinBank::Atom::setCode(const QString & str)
{
  QString ls = str                      ;
  if (ls.length()<3)                    {
    for (int i=ls.length();i<3;i++)     {
      ls.append(' ')                    ;
    }                                   ;
  }                                     ;
  ls = ls . toLower ( )                 ;
  for (int i = 1; i < MAXELEMNO; i++)   {
    const char * tsim = Atoms[i].symbol ;
    QString      astr                   ;
    astr . append ( tsim[0] )           ;
    astr . append ( tsim[1] )           ;
    astr . append ( tsim[2] )           ;
    astr = astr . toLower ( )           ;
    if ( ls == astr )                   {
      Code = i                          ;
      return                            ;
    }                                   ;
  }                                     ;
  Code = 0                              ;
}
